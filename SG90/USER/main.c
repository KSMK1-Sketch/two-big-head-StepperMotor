#include "A_include.h"

/********************************************************************************************************************
*                  ------------------------------------
LEDR      B18
LEDG      B19
LEDB      B20
*                  ------------------------------------
********************************************************************************************************************/


int main(void)
{
    int i = 0;

    SYSCFG_DL_init();

    Set_Servo_Angle(180);
    delay_ms(1000);
    Set_Servo_Angle(0);
    delay_ms(1000);

    while(1)
    {
        Set_Servo_Angle(i++);
        if( i >= 180 )
        {
            i = 0;
        }

        delay_ms(10);
    }
}